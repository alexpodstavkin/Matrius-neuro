/**
 * Заменяет пробел после короткого слова (предлог/союз) на неразрывный пробел —
 * убирает «висячие предлоги» в конце строк.
 */
const SHORT_WORDS = [
  'в', 'во', 'на', 'над', 'не', 'ни', 'но', 'и', 'а', 'у', 'о', 'об', 'от',
  'с', 'со', 'к', 'ко', 'до', 'за', 'из', 'из-за', 'по', 'для', 'без',
  'через', 'под', 'при', 'же', 'ли', 'или', 'про', 'это',
];

const RE = new RegExp(`(^|\\s|[(\\[—–-])(${SHORT_WORDS.join('|')})\\s`, 'giu');

export function nbsp(text: string): string {
  return text.replace(RE, '$1$2 ');
}
