// Маленькие предлоги и союзы — не должны висеть в конце строки.
// Заменяем пробел ПОСЛЕ них на неразрывный пробел (U+00A0).

const NBSP = String.fromCharCode(0xa0)

const HANG_AFTER = [
  'В','в','На','на','С','с','К','к','У','у','О','о','По','по','За','за',
  'И','и','А','а','До','до','От','от','Из','из','Не','не','Ни','ни',
  'Во','во','Со','со','Ко','ко','Об','об','Обо','обо',
  'Для','для','Без','без','При','при','Или','или','Но','но',
  'Что','что','Как','как','Это','это','Мы','мы','Ты','ты',
]

const HANG_BEFORE = ['же','бы','ли','ль','б']

export function nb(text: string): string {
  if (!text) return text
  let out = text.replace(
    new RegExp(`(^|[\\s(])(${HANG_AFTER.join('|')})\\s+`, 'g'),
    `$1$2${NBSP}`
  )
  out = out.replace(
    new RegExp(`\\s(${HANG_BEFORE.join('|')})(?=[\\s.,!?:;)]|$)`, 'g'),
    `${NBSP}$1`
  )
  // Число + любое слово на кириллице (1 сентября, 20 минут, 30 баллов и т.д.)
  out = out.replace(/(\d+)\s+([а-яёА-ЯЁ]+)/g, `$1${NBSP}$2`)
  out = out.replace(/(\d)\s+(%|₽|руб|мин|сек|ч|кг|г|м|км)/g, `$1${NBSP}$2`)
  out = out.replace(/\s—\s/g, `${NBSP}— `)
  return out
}
